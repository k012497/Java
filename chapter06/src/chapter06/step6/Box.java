package chapter06.step6;

public class Box {
	private int height;
	private int width;
	private int length;
	private boolean empty;
	
	public Box() {
		this.height = 0;
		this.length = 0;
		this.height = 0;
	}
	
	public Box(int height, int width, int length) {
		this.height = height;
		this.width = width;
		this.length = length;
		this.empty = true;
	}
}
